const filePath = path.join('context', 'translations.ts');
let content = fs.readFileSync(filePath, 'utf8');

const langOrder = ['ta', 'ml', 'kn', 'te', 'hi'];
const langEnds = {
  ta: '  ml: {',
  ml: '  kn: {',
  kn: '  te: {',
  te: '  hi: {',
  hi: '};',
};

let injected = 0;

for (const lang of langOrder) {
  const entries = missing[lang];
  if (!entries) continue;

  const endMarker = langEnds[lang];
  const sectionStart = content.indexOf(`  ${lang}: {`);
  const sectionEnd = content.indexOf(endMarker, sectionStart);
  const section = content.substring(sectionStart, sectionEnd);

  const newLines = [];
  for (const [key, val] of Object.entries(entries)) {
    const jsonKey = JSON.stringify(key);
    if (!section.includes(jsonKey + ':')) {
      // Find the closing },  of the section to insert before it
      newLines.push(`    ${jsonKey}: ${JSON.stringify(val)},`);
      injected++;
    }
  }

  if (newLines.length === 0) continue;

  // Insert before the last  }, of this language block
  const insertBefore = content.lastIndexOf('\n  },\n' + endMarker, sectionEnd);
  const insertAt = insertBefore + '\n  },\n'.length - '\n  },\n'.length;

  // Find the actual closing }, for this block
  const closingBrace = content.indexOf('\n  },\n', sectionStart);
  const toInsert = '\n    // Additional translations\n' + newLines.join('\n') + '\n';
  content = content.substring(0, closingBrace) + toInsert + content.substring(closingBrace);
}

fs.writeFileSync(filePath, content, 'utf8');
console.log(`Done! Injected ${injected} new translation entries across all languages.`);
